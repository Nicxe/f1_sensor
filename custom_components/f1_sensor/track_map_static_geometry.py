def get_static_track_geometry_provenance(
    *,
    circuit_id: str | None = None,
    circuit_key: str | None = None,
) -> StaticTrackGeometryProvenance | None:
    """Return provenance metadata for a static catalog entry."""
    if circuit_id is None and circuit_key is not None:
        entry = STATIC_TRACK_GEOMETRIES.get(circuit_key)
        if entry is not None:
            circuit_id = entry["circuit_id"]
    if circuit_id is None:
        return None
    provenance = STATIC_TRACK_GEOMETRY_PROVENANCE_BY_CIRCUIT_ID.get(circuit_id)
    if provenance is None:
        return None
    return provenance.copy()


